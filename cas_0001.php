          <p> Beginning in fiscal 
            1995-96 the University has been funding the cost of a recycling and hazardous 
            materials program through a charge placed on the purchase of most expendable 
            items and equipment. The rate of the charge in fiscal 1995-96 and fiscal 
            1996-97 was developed to cover the cost of both the recycling and the 
            hazardous materials programs. During this time both programs were managed 
            by the Environmental Health and Safety Division of Fiscal Affairs. </p>
          <p> In the current fiscal 
            year several changes took place in the program that were initiated as 
            a result of the University ensuring it conformed to cost accounting standards. 
            Acting upon the advice of a consultant, the recycling and hazardous materials 
            programs were separated and distinct rates were established for recycling 
            and for hazardous materials. </p>
          <p> The recycling program 
            was to be funded by a charge on all expendables and equipment purchases 
            except for three categories of purchases: gases, radioisotopes, and chemicals 
            and laboratory supplies. The hazardous materials program would be funded 
            solely from expenditures on the purchase of gases, radioisotopes, and 
            chemicals and laboratory supplies. </p>
          <p> At this same time 
            the Vice President for Fiscal Affairs and Chancellor of the Lexington 
            Campus agreed on the transfer of the recycling program to the Lexington 
            Campus Physical Plant Division. Both sectors of the University, Fiscal 
            Affairs and the Lexington Campus, independently developed charge rates 
            based on the estimated purchases of items within the expenditure codes 
            applicable to the separate programs. </p>
          <p> Starting July 1, 
            1997 the separate rates in support of each program went into effect. Through 
            the monitoring of expenditures it became readily apparent that there was 
            going to be a problem with the hazardous materials rate. The rate was 
            specific to three (3) categories of expenditures and significantly higher 
            than the recycling rate which is charged to most other expenditures of 
            supplies and equipment. </p>
          <p> An examination of 
            expenditure documents has revealed significant shifts in the use of expenditure 
            categories. As a result, Environmental Health and Safety and Fiscal Affairs 
            had to increase significantly the hazardous materials rate to cover estimated 
            program expenses during fiscal 1997-98. Other options were considered, 
            but a determination was made that they would not improve the situation 
            and only add to the costs. </p>
          <p> In order to resolve 
            the problem for the benefit of the entire University, the Environmental 
            Health and Safety Division of Fiscal Affairs and the Physical Plant Division 
            of the Lexington Campus have agreed to a cooperative effort. Effective 
            July 1, 1998 the Environmental Health and Safety Division of Fiscal Affairs 
            and the Physical Plant Division of the Lexington Campus will: </p>
          <ol>
            <li>Cooperate in the 
              establishment of one rate to be placed on the expenditure on supplies 
              and equipment. This will require each program to determine needs, agree 
              to a rate, and a distribution of that rate based upon the expressed 
              needs of each program. This will take place during the preparation of 
              the annual budget.</li>
            <li>Join in the establishment 
              of one service center with separate cost centers for the recycling and 
              hazardous materials programs. Both programs will remain with their respective 
              units and sectors.</li>
            <li>Cooperate and agree, 
              with prior consent, on any rate changes that might be required during 
              any fiscal year for each or either program to meet expenses.</li>
            <li>Any balances remaining 
              at the end of a fiscal year, consistent with cost accounting standards, 
              would be redistributed as income in the new fiscal year based upon the 
              rate distribution for the new fiscal year.</li>
            <li>Representatives 
              from each sector will meet no less than quarterly, or as needed, to 
              review the status of each programs revenue and expenses and determine 
              if changes in the rate or rate distribution may be required for total 
              balance in the service center. </li>
          </ol>
