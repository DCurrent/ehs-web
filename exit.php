			<h1>Setup and Exit Survey Checklist</h1>
		  <p>It is 
the responsibility of all laboratory directors, faculty, and principal <br />
investigators conducting laboratory research to ensure that the necessary <br />
procedures are followed when setting up or exiting a laboratory. If a <br />
new lab intends to have materials listed below please, contact the appropriate <br />
office. For labs exiting a space, follow all applicable actions indicated <br />
below. </p>
		  <h2>Biological Agents Or Human Tissue - <a href="biosafety">Biological Safety</a></h2>
		  <ul>
		    <li> Submit an inventory           of infectious agents to UK Biological Safety Officer.</li>
		    <li>Contact the <a href="biosafety">Biological Safety Office</a> if moving a biological           safety cabinet or if a cabinet is being left in the vacated lab.<br />
  &bull; Read and follow procedures for equipment that has biohazard labels           - <a href="ep/surplus.html">Surplusing Lab and Medical Equipment</a>.<br />
  &bull; Contact <a href="biosafety">Biosafety</a> so an evaluation can be performed           following cleaning and decontamination of the lab space.</li>
	      </ul>
<h2>Chemicals - <a href="./">Occupational Health And Safety</a></h2>
		  <ul>
		    <li>Clean all surfaces.</li>
		    <li>Label all chemicals.</li>
		    <li>Keep only chemicals that are in good quality containers.</li>
		    <li>Remove all chemicals when vacating the laboratory space.</li>
		    <li>Recycle unwanted usable chemicals within the department or contribute 
		    to the <a href="hmm/chemrecycle.html">Free Chemical List</a>.</li>
		    <li>Ticket expired or unwanted chemicals for <a href="//www.etrax.uky.edu">Hazardous 
		    Waste Disposal</a>.</li>
		    <li>Fill out the <a href="ohs/perch_check.php">Perchloric Acid Questionnaire</a> if perchloric acid was used in the laboratory.</li>
		    <li>Submit a hazardous chemical inventory to Occupational Health and 
		    Safety (OHS) when shipping and/or receiving chemicals during lab set or 
		    exit are planned. Some chemicals are <a href="hmm/moving.html">strictly prohibited from transport</a>.</li>
	      </ul>
		  <h2>Hazardous Waste - <a href="/env">Environmental Management</a></h2>
		  <ul>
		    <li>  Utilize the <a href="//www.etrax.uky.edu">E-Trax</a> system to have
	        hazardous waste picked up. This needs to be done a least one week before the           move.</li>
		    <li> Conduct a closeout survey for each laboratory. Contact <a href="/env/">Environmental           Management</a> at least one week before the move to schedule the           survey. </li>
	      </ul>
		  <h2>Radioactive Materials - <a href="radiation">Radiation Safety</a></h2>
		  <ul>
		    <li>Submit <a href="radiation/wasteticket.php">Radioactive             Waste Disposal tickets</a> so radioactive waste will be removed.</li>
		    <li>Contact <a href="radiation">Radiation Safety</a> to schedule a laboratory             survey one week before the scheduled move.</li>
	      </ul>
