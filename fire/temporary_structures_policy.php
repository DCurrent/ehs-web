                    <h1>Temporary Structures Policy</h1>
                    <h2>Objective</h2>
                      <p>This  policy  sets forth the requirements for  the permitting and approval of any tent or other  temporary structure erected on any property owned by the University of Kentucky (UK). It also provides  standards  for the safe operation  and use of the temporary structure.</p>
				
                  <h2>Applicability</h2>
                      <p>This policy applies to any tent or other temporary structure  erected, operated, or maintained for any purpose on any property owned by  UK. The policy requirements shall  apply to all temporary structures erected on the main campus  for clinical  operations,     athletic events, and for  the establishment of a  satellite campus used for agricultural purposes and owned by the University.</p>
                  <p> Tents not exceeding 400 square feet used exclusively by athletic  event patrons outside of a stadium or arena shall be excluded from this policy.  Additionally, tents not exceeding 400 square feet that have been submitted and  approved by  UK Event Management are excluded by  this policy. Site placement requirements may still apply in either instance.</p>
			<h2>Definitions</h2>
                  <p>Temporary  Structure –  any  structure erected for human occupancy on a temporary basis for less than 180  days within a 12-month period. Temporary structures include, but are not  limited to  membrane structures, tents,  canopies, stages, and platforms.</p>
                    <p>Tents  –  a  structure, enclosure, or shelter, with or without sidewalls, constructed of  pliable material supported in any manner except by air or the contents it  protects.</p>
			<h2>Background</h2>
                      <p>Tents and other temporary structures are commonly used for a  variety of reasons on the  UK campus. Each one of these  structures must meet certain requirements for structural stability, weather  resistance, appropriate egress components, lighting, and method of  installation. To ensure that all temporary structures meet the Kentucky Building  Code (KBC) requirements, each one must be submitted for permitting and approval  through the UK Fire Marshal&rsquo;s Office.<br>
                      The  KBC requires that all temporary  structures erected for human occupancy be submitted to the Authority Having  Jurisdiction (AHJ) for permitting and approval. The UK Fire Marshal&rsquo;s Office is  the AHJ and has provided this policy and the associated documents to assist  users in completing this process.</p> 
			
                    <h2>Temporary Structure Policy</h2>
                      <p>Tents and other temporary structures intended for human  occupancy shall not be erected on any University of Kentucky owned property  before being submitted for permitting, review, and approval by the  UK Fire Marshal&rsquo;s  Office. Any temporary structure found  to be erected without being permitted and approved shall be vacated immediately  and removed at the discretion of the UK Fire  Marshal&rsquo;s Office. If removal of the temporary structure is necessary, the <u>UK </u>Fire Marshal&rsquo;s Office will  notify the responsible department or unit person(s) in writing and the  structure shall be taken down within 48 hours of notification. Monetary fees  associated with removal of the temporary structure will be the responsibility  of the department/unit.</p>
                    
			<h2>Temporary Structure Requirements and Approval Process</h2>
                  <p>All temporary structures as defined previously must be  submitted to and approved by the UK Fire Marshal&rsquo;s Office before placement on  University owned property. The process of approval begins with one of the  following two options:</p>
                  <ul>
                      <li>State model approval - The Kentucky Department of Housing, Building, and Construction administers a  model approval program for temporary structures. Using this process to get  certification for the temporary structure means that any future submittals for  the approved model number will not require the resubmission of drawings and/or  a review of the drawings. If a structure is approved by the state and added to  the program, only the manufacturer, model number, and site placement details  will need to be submitted to the UK Fire Marshal&rsquo;s Office  for review and approval.<br>
                      For further information, please refer to this document: <a href="http://dhbc.ky.gov/Documents/KHBC_%20TENT%20Temporary%20Structure%20Guide%20rev%2011-12-13%20Final.pdf" target="_blank">KY Temporary Structure Guidelines</a></li>
                      <li>Event and site-specific engineering analysis - If no state model approval exists, tents and other temporary structure  submissions shall include the required construction and site documents along  with an engineering analysis completed and bearing the required signature and  seal of a registered design professional.</li>
                    </ul>
                  <p>The application documents required for the University Fire  Marshal are found here:</p>
                  <ul>
                    <li><a href="https://app.smartsheet.com/b/form/766a5ff463a24c07b720cbe8de366597" target="_blank">Temporary Structure Model Sumbittal Form</a> – To be  completed by the representative supplying the temporary structure.</li>
                    <li><a href="https://app.smartsheet.com/b/form/797fc5021b46463192c23cec07386c2e" target="_blank">Temporary Structure Site Submittal Form</a> – To be  completed by the unit or department requesting the temporary.</li>
                  </ul>
                  <p style="font-weight: bold;">The following requirements will be in effect for all temporary  structures that are approved for placement.<br>
                      </p>
                    <h4>Wind Speed Capacity</h4>
			
                    <ul>
                      <li>No tent, regardless of maximum wind speed noted  on the approval, shall be occupied when sustained wind speeds reach 40 mph or  greater. </li>
                      <li>If the maximum wind speed on the approval is 40  mph or less, the tent shall be vacated when wind speeds reach 25 mph or 75% of  the established wind speed capacity.</li>
                  </ul>
                    <h4>Use Period</h4>
                      Temporary structures shall not be erected for a period of  more than 180 days. Structures that may need an additional amount of time must  be resubmitted at the end of the period.
  <h4>Location</h4>
                      Temporary structures shall not be located within 20 feet of  lot lines, buildings, other temporary structures, or parked vehicles without  prior approval from the UK Fire Marshal&rsquo;s Office.<br>
  <h4>Means of Egress</h4>
                    <ul>
                      <li>Exits shall be spaced at approximately equal  intervals around the perimeter of the structure and located such that all  points are 100 feet or less from an exit.</li>
                      <li>The number of exits provided shall not be less  than the number of exits shown below:<br>
                        <br>
                      
                      <table>
                        <tr>
                          <th>Occupant Load</th>
                          <th>Minimum Number of Exits</th>
                          <th>Minimum Width of Each Exit</th>
                        </tr>
