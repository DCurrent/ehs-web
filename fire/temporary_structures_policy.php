                          <td>10-199</td>
                          <td>2</td>
                          <td>72 inches</td>
                        </tr>
                        <tr>
                          <td>200-499</td>
                          <td>3</td>
                          <td>72 inches</td>
                        </tr>
                        <tr>
                          <td>500-999</td>
