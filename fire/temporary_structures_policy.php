                        </tr>
                        <tr>
                          <td>1,000-1,999</td>
                          <td>5</td>
                          <td>120 inches</td>
                        </tr>
                      </table>
						</li>
                    
                      <li>Exit  openings may be covered with a contrasting colored, flame-resistant  curtain. The curtain must be free-sliding and mounted on a metal support at  least 80 inches above the ground.</li>
                      <li>Aisles must be provided for in the furniture and  equipment placement. Aisles must be at least 44&rdquo; in width and maintained  throughout the use of the structure. Larger aisles may be required with  increased occupant loads.</li>
                      <li>Structures that are publicly accessible must  have smooth-surfaced aisles for ADA access, subject to approval  by  the UK Fire Marshal&rsquo;s Office.</li>
                      <li>Exits  shall be clearly marked. Exit signs shall be installed at required exits and  will require external or internal illumination of the sign when the structure  is occupied. Power to illuminate the exit signs shall be supplied from a  separate circuit or source of power.</li>
                      <li>Means of egress shall always be illuminated when  the structure is occupied. Fixtures required to provide this illumination shall  be supplied from a separate circuit or source of power.</li>
                    </ul>
                    <h4>Certification Notice</h4>
                      <p>A sign shall be affixed to the tent that states the manufacturer,  model number, UK Fire Marshal&rsquo;s Office approval number, occupancy  number, and maximum wind speed capacity.</p>
                    <h4>General Requirements</h4>
                      <p>General requirements for the use and operation of the temporary  structures. It will be the responsibility of the department/unit to ensure that  all requirements listed in this policy are met and maintained. Inspections shall  be conducted frequently by the department/unit and immediately after any severe  weather event. </p>
                    <ul>
                      <li>All tents, canopies, and membrane structures  shall be constructed of materials that meet the flame propagation performance  criteria of NFPA 701 or other testing standard approved by the UK Fire  Marshal&rsquo;s Office.
                      </li>
                      <li>Electrical installations must be submitted to,  approved, and inspected by the Kentucky Housing, Building, and Construction  Electrical Division. 
                      </li>
                      <li>Required egress as noted in the previous section  shall be available and maintained at all times.
                      </li>
                      <li>Anchoring ropes and/or guylines shall be flagged  to be easily recognizable. Anchors extending above the ground shall be capped.<br>
                      </li>
                      <li>Combustible materials such as hay, straw,  shavings, or similar items shall not be located inside of any temporary  structure. The area inside and surrounding the structure within 20 feet shall  also remain clear of any combustible items unless approved by the UK Fire  Marshal&rsquo;s Office. If these items are approved for use, they must be wetted thoroughly  before occupancy of the structure.<br>
                      </li>
                      <li>Smoking shall not be permitted inside of or near  any temporary structure.<br>
                      </li>
                      <li>Open flames or any devices emitting flame, fire  or heat, or any flammable or combustible liquids, gas, charcoal, or other  cooking devices shall not be located inside or within 20 feet of the temporary structure.<br>
                      </li>
                      <li>Fireworks shall not be used within 100 feet of a  temporary structure and any fireworks must be approved by the UK Fire Marshal&rsquo;s  Office at least 30 days before use.<br>
                      </li>
                      <li>Spot lighting shall be protected from  combustible construction located within 6 feet with approved methods.<br>
                      </li>
                      <li>There shall be a minimum clearance of 3 feet  between the fabric envelope and all contents located inside the temporary  structure.<br>
                      </li>
                      <li>Portable fire extinguishers shall be provided.<br>
                      </li>
                      <li>Heating equipment must be approved for use and  placement by the UK Fire Marshal&rsquo;s Office.<br>
                      </li>
                      <li>LP gas tanks approved for use must be located at  least 10 feet from the structure. The tank and all appurtenances shall be  protected from access by the general public.<br>
                      </li>
                      <li>Generators approved for use must be located at  least 20 feet from the structure. The generator and all wiring shall be  protected from access by the general public.<br>
                      </li>
                      <li>Flammable and combustible liquids shall not be  used or stored inside temporary structures. If approved, these materials must  be stored at least 50 feet from the structure.</li>
                    </ul>
