                    <h1>Fact Sheet - Lead</h1>
                  <p>Lead is a naturally occurring, soft,  bluish-gray heavy metal. Although nearly 50% of lead used today comes from  recycled materials such as car batteries, its most common source is the mineral  Galena (lead sulfide) that forms as pockets or veins in carbonate rock. </p>
                  <p>Lead has been in use since ancient  times.  A lead statue discovered in  Turkey has been dated to around 6500 BC.  The Romans had indoor plumbing that was made  from sheets of lead rolled into pipe.  Plumbum,  the Latin word for lead, is also the origin of the word plumber and the metal&rsquo;s  elemental symbol (Pb).  Industrial  emissions, combustion of leaded gasoline and widespread use of lead-based paint  have all contributed to a vast amount of air, water and soil pollution in the  20th Century.</p>

<h2>Lead Containing Materials</h2>
<p>Due to its abundance, low cost and  physical properties (low melting point, corrosion resistance, waterproof nature  and malleability) lead and lead compounds have been utilized in a variety of  products including:</p>

<ul class="columns">
  <li>Ammunition and fishing sinkers</li>
  <li>Paint and varnish</li>
  <li>Ceramic glaze, glass, and crystal </li>
  <li>Pipes, faucets, and solders	o	Caulking</li>
  <li>X-ray shielding </li>
  <li>Plastics</li>
  <li>Batteries</li>
  <li>Cable covers</li>
  <li>Gasoline</li>
  <li>Metal flashing</li>
  <li>Tank liners</li>
  <li>Brass, bronze, and pewter alloys</li>
  <li>Pesticides</li>
  <li>Folk medicines (Mexico)</li>
  <li>Cosmetics and hair dyes (India)</li>
  <li>Imitation pearls</li>
  <li>Crayons (china) </li>
  </ul>
<p>Regulatory agencies have banned the use of lead compounds in some  products linked to public health disease or environmental damage, such as  gasoline additives, soldered food cans, household paints and toys. Lead-acid  batteries account for about two thirds of lead still used in the U.S. today.</p>
<h2>Health Effects</h2>
<p>Acute short term health effects may include:</p>
<ul class="columns">
        <li> Cramps (lead colic) 	</li>
                      <li>Irritability and moodiness</li>
                      <li>Headaches</li>
                      <li>Insomnia</li>
                      <li>Tiredness</li>
                      <li>Nausea</li>
                      <li>Loss of libido</li>
                      <li>Constipation</li>
                      <li>Birth defects</li>
                      <li>Miscarriage</li>
                      <li>Stillbirth</li>
                      <li> In children: hyperactivity, lower IQ, slowed growth, hearing loss </li>
                    </ul>
<p>Chronic, long term health effects may include:</p>
<ul class="columns">
  <li>Muscle and joint soreness</li>
  <li>Numbness</li>
  <li>Fine tremors</li>
  <li>Anemia</li>
  <li>Hypertension</li>
  <li>Infertility</li>
  <li>Kidney damage </li>
  </ul>
<p>Lead can stay in the body for years and is stored in bone or soft tissue  including the liver and kidneys.  During  periods of high calcium demand such as pregnancy, menopause and aging, lead  stored in bone tissue can be released back into the bloodstream. Lead is also  able to cross the placenta and blood/brain barrier.</p>
<h2>Exposure Risks</h2>
<p>In terms of reducing one&rsquo;s risk, the  two areas of greatest concern are the workplace and the home. On the job, high  exposures have been associated with lead smelters; construction work that  involves sanding, grinding, blasting, torch-cutting or welding surfaces covered  with lead paint; auto shops and plumbers who use lead solders; artists and  ceramic workers who use lead glazes, and indoor firing ranges. Studies have  shown that lead dust can be carried on work clothing and contaminate worker&rsquo;s  cars, homes and family.</p>
<p>At  home, the most common exposure to lead comes from deteriorating paints applied  before 1978. Children under age seven are at greatest risk of ingesting lead  through hand to mouth contact after touching contaminated surfaces or soil.  Other potential sources of exposure in the home include leaded crystal,  dinnerware glazes, lead water pipes and pipe joint solder, faucets with brass  fittings, vinyl mini-blinds made in China, folk medicines and calcium  supplements made from animal bone.</p>
<h2>Protective Measures - General</h2>
<p>UK Environmental Management Department (EMD) requires the use of strict  control measures whenever construction, renovation or maintenance activities  may disturb lead-based paint or other lead-containing materials on campus.  Numerous local, state and federal laws also mandate control methods designed to  protect workers, children and the environment from exposure to lead.</p>
<h2>Protective Measures - Individual</h2>
<p>Use the following measures to protect  you and others from exposure to lead at work and at home.</p>
<ul>
  <li>Report damage or deterioration of painted       surfaces at work to your supervisor, building operator, EMD, or UK       Occupational Health &amp; Safety.</li>
  <li>Presume all paints and varnishes applied       before 1980 contain lead including finishes on old toys, furniture and       playground equipment.</li>
  <li>Consider having young children tested for       lead even if they appear healthy.</li>
  <li>Wash children&rsquo;s hands and toys often.</li>
  <li>Regularly clean horizontal surfaces that       children can reach, such as floors and window sills, with TSP or other phosphorus-based       cleaner.</li>
  <li>Avoid exposure to lead dust when       remodeling or renovating your home.</li>
  <li>Repair peeling, chipping or chalking       paint; keep paint chips out of the soil.</li>
  <li>Do not dry sand, grind or burn painted       surfaces; use wet sanding methods to prepare surfaces for re-painting and       wash your hands prior to eating, drinking or smoking.</li>
  <li>Do not use a vacuum to pick up paint       chips unless the vacuum has a HEPA filter.</li>
  <li>Plant  shrubs, ground cover or other physical barrier along the exterior drip line of  your home to keep children and pets away from potentially contaminated soil. </li>
</ul>
