			<h1>Emergency Eyewash And Shower Equipment</h1>
			<h2>General</h2>
			<p>The Kentucky Occupational Safety and Health Administration (OSHA) has           adopted a modified version of the 1990 American National Standards Institute's           Standard for emergency eyewash and safety shower equipment. OSHA requires:           &quot;Where the eyes or body of any person may be exposed to injurious corrosive           material, suitable facilities for quick drenching or flushing of the eyes           and body shall be provided within the work area for emergency use.&quot; The           following outline the University's technical standards and preferences           to provide safety equipment to meet this mandate: </p>
            <ul>
              <li>Plans and drawings             must show location of safety shower and eyewash units; specifications             must be provided for review &amp; approval. </li>
          </ul>
            <h2> Physical Requirements           for Emergency Safety Shower </h2>
            <ul>
              <li>Emergency safety             showers shall be located in an immediately accessible area within the             laboratory unit or other work areas where the user shall not have to             pass through a corridor door to reach the unit. </li>
              <li>Emergency safety             showers shall be identified with a highly visible sign and a green cross             located as an integral part of the floor directly under the shower. </li>
              <li>Safety showers             installed in remote locations must be provided with an audible warning             buzzer and visual alarm the audible and visual alarms shall be different             than those used for fire, but the devices need not be larger or louder             than those for fire alarms. The buzzer and visual alarm will be located             above the door of the room with the shower. If feasible, an alternative             alarm method connecting flow valves electronically to a twenty-four             hour per day, seven days per week monitoring facility is preferred. </li>
          </ul>
            <h2> Physical Requirements           for Emergency Eyewash Equipment </h2>
            <p> Emergency eyewash           equipment shall ensure that a controlled flow of potable water is provided           to both eyes simultaneously at a velocity low enough not to be injurious           to the user following, as a minimum, the guidelines of ANSI Z358.1-1990. </p>
            <ul>
              <li>Emergency eyewash             units shall be located in an immediately accessible area within the             laboratory unit or other work area where the user shall not have to             pass through a door to reach the unit. </li>
              <li>Emergency eyewash             shall be identified with a highly visible sign and if floor or wall             mounted, provided with a green cross located as an integral part of             the floor directly under the eyewash or on the wall directly behind             the eyewash unit. </li>
              <li>There shall be             no sharp projections anywhere in the operating area of the unit. </li>
              <li>The unit shall             be located to provide enough room to allow the eyelids to be held open             with the hands while the eyes are in the water stream. </li>
              <li>The control valve             shall be designed so that the water flow remains in the on position             without the use of the operator's hands and must remain open until manually             shut off. The valve shall be large enough to be easily located and operated             by the user. </li>
            </ul>
            <p> Emergency eyewash           and shower equipment must be installed in every lab that is provided with           a fume hood. Laboratories sharing a common suite or area not separated           by closed doors may find one emergency shower is sufficient but each lab           unit must be equipped with an approved eyewash. </p>
          <p> You can contact the           University of Kentucky Occupational Health and Safety Department for questions           related to this guideline at (859) 257-3862. </p>
