                    <h1><img src="../media/image/ih_asbestos_1.jpg" style="width:300px; height:275px; float:right; margin-left:20px; border-radius:5px;" alt="asbestos">Asbestos</h1>
                    <p>The University of Kentucky is committed to  providing work, study, research, and residential environments that are free from  recognized hazards.  To fulfill this  commitment as well as uphold applicable laws and regulations, UK notifies  affected persons of the presence of asbestos-containing materials (ACM) in  campus buildings.  Contact the UK  Environmental Management Department (EMD) office at 859-323-6280 for more  information about ACM in specific campus buildings.</p>
                    <p>Asbestos is a common name given to a group of  mineral fibers that occur naturally and have been incorporated into a variety  of construction products such as wall and ceiling plasters, floor tile, pipe  insulation and asphalt roofing. These materials pose no risk to health unless  they are disturbed in such a way that asbestos fibers become airborne, are  inhaled and deposited within the lungs. Increased incidence of several  illnesses including asbestosis (a debilitating lung disease), lung cancer, and  mesothelioma (a rare cancer of the pleura or visceral cavity lining) have been  observed in individuals who were persistently exposed to high levels of  airborne asbestos in work environments such as mining, milling, shipbuilding,  construction and manufacturing.</p>
                  <p>UK EMD conducts on-going building surveys to  identify and safely manage previously installed asbestos-containing products.  Furthermore, all renovation of campus buildings must be reviewed in advance by EMD  to ensure that no ACMs are disturbed without proper safeguards. Work that  requires removal or repair of ACM is restricted to trained and qualified  persons only.</p>
                  <p>Use  the following measures to protect you and others from exposure to airborne  asbestos:</p>
                  <ul>
                    <li>Presume all building materials from  pre-1981 buildings contain asbestos until determined otherwise by EMD or the UK  Health &amp; Safety (UKHS) office.</li>
                    <li>Do not remove, cut, drill, sand, grind  or otherwise disturb any material that may contain asbestos.</li>
                    <li>Do not go above ceilings, behind walls  or into building spaces such as attics and crawlspaces unless these areas have  been inspected and cleared by EMD and/or UKHS.</li>
                    <li>Do not pull cable or wiring through  above-ceiling spaces with asbestos.</li>
                    <li>Do not install screws, pins, nails or  hangers into asbestos ceiling or wall plasters.</li>
                    <li>Be careful not to damage walls,  ceilings or floors when moving furniture or equipment.</li>
                    <li>Do not brush, sweep or vacuum textured  asbestos ceiling plaster or plaster debris.</li>
                    <li>Immediately report any observed damage  or deterioration of suspect building materials to your supervisor, building  operator, EMD, or UKHS.</li>
                  </ul>
                  <p>The University of Kentucky is committed to a  policy of safely and effectively managing asbestos on campus. The University  maintains a staff of certified professionals who conduct building material  surveys, coordinate and supervise asbestos construction activities, perform air  monitoring and provide training. </p>
                  <p>Asbestos  survey results listing specific locations where ACM may be encountered within  your building, detailed procedures for working with asbestos and bulk and air  monitoring sample analysis results are available for review and photocopying to  any UK employee.</p>
