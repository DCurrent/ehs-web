			<h1>Laboratory Design Guidelines</h1>
		  <h2>Overview</h2>
		  <p> Remodeling and new construction projects offer an outstanding opportunity           to improve facilities for current-day research and teaching. One aspect           of these improvements is related to safety and health features of the           laboratories. This Guideline is provided to help architects/engineers,           departmental users and others understand the requirements for new and           remodeled University laboratories. </p>
          <h2> Fire Safety &amp;           Building Codes </h2>
          <p> 1) All remodeling           and new construction must be reviewed and approved by the University Fire           Marshal. Labs must be designed for the &quot;moderate hazard&quot; classification           according to the Kentucky Building Code and applicable referenced NFPA           fire codes. For laboratories in excess of &quot;moderate hazard&quot; classification,           an exception must be obtained from the University Fire Marshal. </p>
          <p> 2) Doors </p>
          <ul>
            <li>Doors opening onto             exit corridors must swing with exit egress and must be self-closing             and self-latching. </li>
            <li>Doors within interior             partitions shall be self-latching. </li>
          </ul>
          <p> 3) All door hardware           shall meet the requirements for the physically impaired. </p>
          <p> 4) View panel </p>
          <ul>
            <li>All doors opening             onto the exit corridor and within interior partitions must be provided             with view panels; </li>
            <li>All view panels             shall be 3&quot;x33&quot; and shall be 1/4' wire glass in steel frames. </li>
            <li>All view panels             shall be placed directly above the self-latching hardware. </li>
          </ul>
          <p> 5) Any interior partitions           more than 6' in height shall be extended to the floor slab above. Any           exceptions must be approved by the University Fire Marshal. </p>
          <p> 6) As a minimum,           a 5# ABC (all-purpose) fire extinguisher shall be provided adjacent to           each door giving access to an exit corridor. Additional units may be required           for a particular/specialized hazard(s). </p>
          <p> 7) Interior and/or           windowless laboratories shall be provided with emergency lighting. </p>
          <p> 8) Aisles/passageways           within labs shall be a minimum of 36&quot; of clear and unobstructed width. </p>
          <h2> Engineering Controls </h2>
          <p> 9) Fume hoods must           comply with UK Design Guidelines for Fume Hoods. </p>
          <p> 10) A master gas           shut off must be provided for each laboratory or group of labs. The valve           should be located outside the laboratory adjacent to the means of egress           door of the laboratory and labeled as to its purpose. </p>
          <p> 11) Safety showers           and eyewashes must comply with UK Design Guidelines, and must be installed           in every laboratory that has a fume hood. </p>
          <p> 12) Incorporation           of proper ergonomic principles must be provided. </p>
          <p> 13) Shelves and cabinets           must be built of non-porous material; secured to the wall; and shelves           must have a front lip. </p>
          <p> 14) Placement of           &quot;supply air&quot; and &quot;exhaust air&quot; vents must be located to avoid short-circuited           air movement patterns. Further, in laboratories requiring tremendous volumes           of supply air such as laboratories with multiple fume hoods, low velocity           air diffusers will be required to avoid turbulence and noise. </p>
          <p> 15) Canopy hoods           are generally not acceptable for contaminant exhaust. </p>
          <p> 16) Laboratories           must be designed to pull air into laboratory from the corridor (negative           pressure in relation to rest of the building). </p>
          <p> 17) Electrical outlets           located within six feet of sinks, safety showers, or other sources of           water shall be Ground Fault Circuit Interrupter (GFCI) outlets/circuits. </p>
          <h2> General Considerations </h2>
          <p> 18) Laboratories           must include the following signage features: </p>
          <ul>
            <li>Room Number </li>
            <li>Sign plate large             enough to include department name, user name(s), emergency contacts             etc. </li>
            <li>Appropriate precautionary             signs (e.g. radiation and biohazard) </li>
          </ul>
          <p> 19) Acid corrosive           storage should not be located under fume hoods because acids cause significant           corrosion to the fume hoods over time. Separate storage must be provided           for acids and solvents. </p>
          <p> 20) Underwriter's           laboratory or Factory Mutual approved solvent storage cabinets are required           under fume hoods. These cabinets shall be vented. </p>
          <p> 21) Carpet is not           acceptable for chemical, radioactive, biohazard, or animal use laboratories           or laboratory support spaces. </p>
          <p> 22) Designated break           areas outside the chemical use laboratory area are required. At a minimum,           a work desk/ break area should be separated from the laboratory work area           by a non-combustible transparent barrier to isolate the workers from exposure           to hazards. </p>
          <p> 23) High use radioactive           materials labs may require special engineering and facility features (e.g.           HEPA filters, seamless flooring, stainless steel bench tops). Such laboratories           must be approved by the UK Radiation Safety Officer. </p>
          <h2> Special Use Laboratories </h2>
          <p> 24) Biological Level           3 laboratories require extensive engineering and architectural features           and will be reviewed and approved by the UK Biological Safety Officer           and the Institutional Biosafety Committee. </p>
          <p> Approved by UK Design           Guidelines &amp; Technical Standards Committe June 18, 1998.</p>
