                    <h1>Safety Tips for Manual Material Handling</h1>
                    

				  <p>Manual material handling (MMH) work contributes to a large percentage of the over half a million cases of musculoskeletal disorders reported annually in the United States. Musculoskeletal disorders often involve strains and sprains to the lower back, shoulders, and upper limbs.  The best way to prevent these injuries is to develop working habits that reduce the strain placed on the back, shoulders and upper limbs.   These are some basic tips you can do to help.
                    </p>

 

                  <h3>Plan the lift</h3>
                  <ul>
                    <li> Wear appropriate shoes to avoid slips, trips, or falls.</li>
                    <li> If you wear gloves, choose the size that fits properly.</li>
                    <li> Slide, push, or roll instead of carrying, when appropriate.</li>
                    <li>Minimize the distances loads are lifted and lowered.</li>
                    <li>Lift only as much as you can safely handle by yourself.  Anything above 50 lbs or irregular in size should be lifted with a partner.</li>
                    <li>Use extra caution when lifting loads that may be unstable.                      </li>
                    <li>Keep the lifts in your power zone (i.e., above the knees, below the shoulders, and close to the body), if possible.</li>
                 </ul>
                      
                      
                      
                      
                      
                      
                      
                      
                      
                      
                      
                      
                      
                    
                  <h3>When carrying</h3>
                  <ul>
                    <li>
                      
                    Squat down to lift the object, but keep your heels off the floor. Get as close to the object as you can.</li>
                    <li>Use your palms (not just your fingers) to get a secure grip on the load.  Make sure you will be able to maintain a hold on the object without switching
                      
                      your grip while in transit.                    </li>
                    <li>Lift gradually (without jerking) using your leg, abdominal and buttock muscles and keeping the load as close to you as possible. Keep your chin
                      
                      tucked in so as to keep a relatively straight back and neck line.                    </li>
                    <li>Once you're standing, change directions by pointing your feet in the direction you want to go and turning your whole body. Avoid twisting at your waist while carrying a load.                    </li>
                    <li>While in transit keep loads close to your body.</li>
                    <li>Use two hands to ensure a secure grip on load.  If you have to carry a load with one hand, alternate hands for long distances.</li>
                    <li>Alternate heavy or forceful exertion tasks with less physically demanding tasks.</li>
                    <li>Always take rest breaks.

