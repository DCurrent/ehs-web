                    <p>When working or spending extended time in high temperatures,  remember to do the following:</p>
                  <ul>
                      <li>Hydrate properly the night before, avoiding  alcohol and caffeine</li>
                      <li>Wear light, breathable clothing</li>
                      <li>Drink water every 15 minutes even if you are not  thirsty</li>
                      <li>Eat foods high in water content, such as  watermelon, lettuce, and cucumbers</li>
                      <li>Take frequent rest breaks in shade or air  conditioned rooms</li>
                      <li>Wear sunscreen to protect your skin</li>
                      <li>Listen to your body, and watch out for symptoms  of heat stress in yourself and those around you</li>
                    </ul>
