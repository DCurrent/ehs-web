                  <p>Heat Illness is a potentially serious medical condition that  affects millions of people around the United States every summer. If you&rsquo;ve  ever lived in Kentucky and its humid subtropical climate, you know that  summertime temperature can quickly become dangerous within the Bluegrass state.  When high temperatures combine with high humidity, heat indices can easily rise  over 100°F, leaving even the strongest among us vulnerable to heat illness. </p>
                  <p><img src="../media/image/ohs_heat_awareness_chart.png" alt="Chart"></p>
                    <p>
                      When exerting yourself in extreme heat, your body tries to  protect itself in several ways. The human system works best when it is at the  preferred 98.6°F, so 80% of the energy you use leaves your body in the form of  heat through the circulatory system.  The  heat travels through the blood to the skin where it dissipates naturally. In  normal circumstances, you can feel your skin temperature rise as your body  works to draw heat away from vital internal organs. When you are subjected to  higher temperatures, the body cannot dissipate heat fast enough through dry  skin, so your brain will activate the sweat glands of your body to activate,  sending fluids to the surface of the skin. The fluids help draw heat more  efficiently from the skin, and the heat is dissipated through evaporating  sweat. Once your body transfers the heat to your skin and sweat, the cooled  blood circulates back through to your vital organs. <br>
  <br>
                  However, if you are in an environment in which the temperature is higher that  your body&rsquo;s temperature, or if you&rsquo;re in an extremely humid environment, sweat  is unable to evaporate from your skin, and the heat cannot safety dissipate.  Once this happens, your body begins to send out warning signals to let you know  that something is wrong.</p>
                    <ul>
                      <li><a href="http://www.webmd.com/skin-problems-and-treatments/tc/heat-rash-topic-overview">Heat Rash</a> can occur when sweat  doesn&rsquo;t properly evaporate from the skin, causing irritating and uncomfortable bumps on the affected area.</li>
                      <li> <a href="http://www.webmd.com/fitness-exercise/heat-cramps">Heat Cramps</a> can occur when a tired or overworked muscle loses electrolytes,  such as salt, through natural sweating, that it begins to seize. These can be very painful and last for hours.</li>
                      <li> <a href="http://www.webmd.com/fitness-exercise/heat-exhaustion">Heat Exhaustion</a> can occur when the  body becomes dangerously dehydrated of  water, salt, or both. An affected person can have a throbbing headache, become confused and/or irritable, have a rapid  heartbeat, and pale skin. The body will produce excess sweat in a last  ditch effort to cool internal organs. If  not treated quickly and properly, a person can begin to experience the most  dangerous heat illness of all, heat stroke.</li>
                      <li><a href="http://www.webmd.com/a-to-z-guides/heat-stroke-symptoms-and-treatment">Heat Stroke</a> occurs when the body&rsquo;s cooling system fails, and is  considered a medical emergency. The  victim becomes dehydrated and will no longer sweat. With no means to cool  itself, body temperature will quickly  rise to above 104°F. After 30 minutes at that temperature, cells begin to  break down, causing potentially deadly  results. Victims will often faint when suffering a heat stroke, as well as  potentially vomiting, having hot, red skin, shallow breathing, and a rapid  heartbeat. If you suspect someone is suffering a heat stroke, dial 911  immediately.</li>
                    </ul>
                    <p>Luckily, victims of heat illness can make a full recovery  with proper care. Please watch this short video for a more thorough explanation  of the body&rsquo;s cooling system, and how to treat those affected by heat-related  illnesses. </p>
                     
