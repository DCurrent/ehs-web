                    <h1 id="def">Automated External Defibrillator (AED)</h1>
                    <h3 class="center"><a href="https://maps.uky.edu/aed/" target="_blank" class="no_icon">Click here for a map of AED locations</a></h3>
                  <p>An AED (automated external defibrillator) is a piece of medical equipment used to treat cardiac<br>
                      arrhythmias and cardiac arrest. It automatically diagnoses the hearts rhythm to determine if a shock<br>
                      needs to be administered to a person, and will deliver that shock if needed. As technology for this<br>
                      equipment has improved and the cost to purchase them has decreased over the last several years, more<br>
                  AEDs are being used in public and private settings.</p>
                  <p>                      While studies done by the American Heart Association (AHA) show that AEDs can in fact save lives, the<br>
                      use of onsite portable AEDs may not always be the most effective care that can be provided to someone<br>
                      in cardiac arrest on our campus. The Ontario Pre-hospital Advanced Life Support Study (OPALS) is the<br>
                      gold standard in cardiac arrest studies. It found that locations that were able to provide optimal rapid<br>
                      defibrillation (emergency personnel administering shock in less than 8 minutes) were effective in the<br>
                      survival of a person suffering from cardiac arrest. Ability of EMS to respond quickly, CPR and AED<br>
                      training, and the appropriate placement of maintained AEDs are important keys in the improvement of<br>
                      survival. Many areas on the University of Kentucky’s campus have less than an 8 minute response time<br>
                      for emergency personnel and therefore an AED may not be necessary. UK Occupational Health &amp; Safety<br>
                      requires that departments submit an official <a href="http://ehs.uky.edu/apps/shocker">request form</a> for approval before procuring an AED so that a<br>
                    review of your specific need for having this regulated piece of equipment can be done.</p>
                  <p><a href="#def">Top of Page</a></p>
                  <h2 id="why_contact">Why should Occupational Health &amp; Safety be contacted before a
                    department/building purchases an AED?</h2>
                    
                  <p>OH&amp;S wants to ensure that AED boxes are installed in easily accessible and visible areas so that trained<br>
                      staff can locate and access them quickly. We also want to make sure that they are maintained<br>
                      appropriately and used in a safe manner. This means that each building or area that has or intends to<br>
                      procure and AED has someone assigned to manage the equipment and perform all other duties that are<br>
                    required by law when in possession of one.</p>
                  <p><a href="#def">Top of Page</a></p>
                  <h2 id="regulation">What are the regulations that must be followed if an AED is purchased?</h2>
                  <p id="top"><a href="#667">KRS 311.667</a> requires the following:</p>
                    <ul>
                      <li>If you acquire an AED you must inform an agent of the local emergency medical services system<br>
and the local emergency communications or vehicle dispatch center. The location and type of<br>
AED acquired must be given to this agent (i.e. local 911/dispatch).</li>
                      <li>People that will be expected to use the AED must be properly trained in CPR and AED use.</li>
                      <li>Maintenance and testing of the AED must be done in accordance with the guidelines set by the</li>
                      <li>manufacturer of the AED equipment.</li>
                      <li>Oversight of your AED program must be by a physician who is licensed to practice in the state of<br>
                        Kentucky.</li>
                      <li>If the AED is used for emergency care or treatment for a person in cardiac arrest, the local<br>
                        emergency medical services system must be notified as soon as possible. The licensed physician<br>
                        performing oversight of your AED program must also be informed. <a href="http://www.lrc.state.ky.us/Statrev/ACTS2000/0016.pdf">Complete KY statute regarding AEDs</a>. </li>
                  </ul>
                    <p><a href="#def">Top of Page</a></p>
                  <h2 id="training">Where can we get trained for AED use?</h2>
                    <p>UK Occupational Health &amp; Safety suggests receiving AED use and CPR training from the American Heart<br>
                      Association or the American Red Cross. Other entities can provide training as long as they provide<br>
                    equivalent nationally recognized courses.</p>
                    <p><a href="http://www.redcross.org/services/hss/courses/aed.html">American Heart Association</a><br>
                      <a href="http://www.americanheart.org/presenter.jhtml?identifier=3012360">American Red Cross</a><br>
                    </p>
                    <p><a href="#def">Top of Page</a></p>
                  <h2 id="samaritan">Does Kentucky have a Good Samaritan law in regards to the use of an
                    AED?</h2>
                    <p>Yes, the state of Kentucky does have a <a href="#668">Good Samaritan law for AED use</a>. This law provides immunity<br>
                      from civil liability for any personal injury that results from the use of an AED for treatment or care as<br>
                      long as they are not grossly negligent in this care. If you have any additional questions about AED use,<br>
                      requirements by law, or if you currently have an AED in your building, please contact UK Occupational<br>
                      Health &amp; Safety at 257-3827. We will be happy to provide assistance or make recommendations about<br>
                  AED procurement or use.</p>
                    <p><a href="#def">Top of Page</a></p>
                    <h2 id="krs_summary">KRS AED Summary</h2>
                    <h3 id="666">311.666 Definitions for KRS 311.665 to 311.669</h3>
                    <p>As used in KRS 311.665 to 311.669, unless the context requires otherwise:</p>
                    <ol>
                      <li>&quot;Automated external defibrillator&quot; or &quot;AED&quot; means an external defibrillator<br>
                        capable of cardiac rhythm analysis which will charge and, with or without<br>
                        further operator action, deliver a shock after electronically detecting and<br>
                        assessing ventricular fibrillation or rapid ventricular tachycardia. These devices<br>
                      are known as fully or semiautomatic defibrillators;</li>
                      <li>&quot;Cardiopulmonary resuscitation&quot; or &quot;CPR&quot; means a basic emergency<br>
                      procedure for life support, consisting of artificial respiration and manual<br>
                  external cardiac massage; and</li>
                      <li> &quot;Emergency medical services system&quot; means the same as in KRS 311A.010.<br>
                  Effective:July 15, 2002</li>
                    </ol>
                    <p>                      History: Amended 2002 Ky. Acts ch. 211, sec. 39, effective July 15, 2002. --<br>
                      Created 2000 Ky. Acts ch. 16, sec. 2, effective July 14, 2000.<br>
                      <br>
                      Legislative Research Commission Note (7/14/2000).􀁢􀁢In codification, a reference<br>
                      in subsection (3) to &quot;KRS 211.950&quot; has been changed to &quot;KRS 311.6521.&quot; See<br>
                  2000 Ky. Acts ch. 343, sec. 1.</p>
                    <p><a href="#def">Top of Page</a></p>
                    <h3 id="667">311.667 Requirements for person or entity acquiring an automated external<br>
                    defibrillator.</h3>
                    <p>In order to ensure public health and safety:<br>
                    </p>
                    <ol>
                      <li> A person or entity who acquires an AED shall ensure that:
                        <ol>
                          <li>  Expected AED users receive American Heart Association or American<br>
                            Red Cross training in CPR and AED use, or an equivalent nationally<br>
                            recognized course in CPR and AED use;</li>
                          <li> The AED is maintained and tested according to the manufacturer's<br>
                            operational guidelines;</li>
                          <li>There is medical oversight of the AED program by a physician licensed in<br>
                            Kentucky to ensure compliance with requirements for training,<br>
                            maintenance, notification, and communication with the local emergency<br>
                            medical services system. The physician providing oversight shall also<br>
                            work with the AED site to establish protocols for AED deployment and<br>
                            conduct a review of each use of an AED; and</li>
                          <li>Any person who renders emergency care or treatment on a person in<br>
                            cardiac arrest by using an AED activates the local emergency medical<br>
                            services system as soon as possible and, if an entity with an AED<br>
                            program, reports any clinical use of the AED to the licensed physician.</li>
                          <li>Any person or entity who acquires an AED shall notify an agent of the local<br>
                            emergency medical services system and the local emergency communications<br>
                            or vehicle dispatch center of the existence, location, and type of AED acquired.<br>
                          </li>
                        </ol>
                      </li>
                    </ol>
                     Effective:July 14, 2000<br>
                     History: Created 2000 Ky. Acts ch. 16, sec. 3, effective July 14, 2000.
                     <p><a href="#def">Top of Page</a></p>
                     <h3 id="668">311.668 Immunity from civil liability for user of automated external
                     defibrillator -- Exemption from KRS 311.667 for Good Samaritan.</h3>
                     <ol>
                       <li>Any person or entity who, in good faith and without compensation, renders<br>
                         emergency care or treatment by the use of an AED shall be immune from civil<br>
                         liability for any personal injury as a result of the care or treatment, or as a result<br>
                         of any act or failure to act in providing or arranging further medical treatment,<br>
                         where the person acts as an ordinary, reasonable prudent person would have<br>
                       acted under the same or similar circumstances.</li>
                       <li>The immunity from civil liability for any personal injury under subsection 
                         <ol>
                           <li> of
                             this section includes the licensed physician who is involved with AED site<br>
                             placement, the person or entity who provides the CPR and AED site<br>
                             placement, the person or entity who provides the CPR and AED training, and<br>
                             the person or entity responsible for the site where the AED is located.<br>
                           </li>
                         </ol>
                       </li>
                       <li> The immunity from civil liability under subsection (1) of this section does not<br>
                         apply if the personal injury results from the gross negligence or willful or<br>
                         wanton misconduct of the person rendering the emergency care.</li>
                       <li>The requirements of KRS 311.667 shall not apply to any individual using an<br>
                         AED in an emergency setting if that individual is acting as a Good Samaritan<br>
                         under KRS 313.035 and 411.148.<br>
                       </li>
                     </ol>
                    <p>Effective:July 15, 2010<br>
                         History: Amended 2010 Ky. Acts ch. 85, sec. 21, effective July 15, 2010. --<br>
                  Created 2000 Ky. Acts ch. 16, sec. 4, effective July 14, 2000.</p>
