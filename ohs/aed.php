                  <h3 id="669">311.669 Professionals to whom KRS 311.665 to 311.669 does not apply --<br>
                    Licensing boards may require continuing education and training.</h3>
                    <ol>
                      <li>The provisions of KRS 311.665 to 311.669 shall not apply to the use of an AED<br>
                        by: 
                          <ol>
                            <li>Physicians, podiatrists, or osteopaths licensed under KRS Chapter 311 or<br>
                              chiropractors licensed under KRS Chapter 312;</li>
                            <li>Physician assistants as defined in KRS 311.550;</li>
                            <li>Registered nurses, practical nurses, or advanced practice registered<br>
                              nurses licensed under KRS Chapter 314;</li>
                            <li>Dentists licensed under KRS Chapter 313; or</li>
                            <li> Paramedics licensed, or first responders or emergency medical<br>
                              technicians certified, under KRS Chapter 311A.</li>
                          </ol>
                      </li>
                      <li> Nothing in this section shall preclude the licensing boards referred to in<br>
                        subsection (1) of this section from requiring continuing education or training on<br>
                        the use of an AED.<br>
                      </li>
                    </ol>
                    
