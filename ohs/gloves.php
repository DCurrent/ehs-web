                    <p>The type of chemical to be             used is the most important factor for selecting gloves. The chemical             resistance of the glove is based on degradation, permeation, and exposure.</p>
                    <p>Degradation             is the measurement of the physical deterioration of the glove due to             contact with the chemical. The material may get harder, stiffer, softer,             or may swell.</p>
                  <p>Permeation             is the result of molecular diffusion of a chemical through a glove material. </p>
                    <p>Exposure             is the length of time the glove will be in contact with the chemical.             (Protection against splashes verses submersion in the chemical).            </p>
                    <h2 id="glove_use">Glove Use</h2>
                    <p>Using the Personal             Protective Equipment (PPE) correctly is another link in the chain of             keeping a person safe. If the PPE is not worn or used correctly then             it may not offer the expected protection to the user. Below are guidelines             for putting on (donning) and removing gloves (doffing), as well as the             appropriate times and places to wear gloves.</p>
                    <h3>Donning             Gloves</h3>
                    <ol>
                      <li>Wash hand             before putting gloves on.</li>
                      <li>Remove all jewelry from hands.</li>
                      <li>Pick up one glove with the right hand.</li>
                      <li>Line the thumb side of the glove up with the thumb side of the             left hand.</li>
                      <li>Slip the open end of the glove over the left hand and thumb.</li>
                      <li>Stretch the palm side of the glove with the right hand, pulling             the glove on to finger level.</li>
                      <li>Position the fingers of the glove in line with the fingers of             the left hand.</li>
                      <li> Pull the remainder of the glove onto the left hand.</li>
                      <li>Pick up the second glove with the gloved left hand.<br />
                      </li>
                      <li>Line the thumb side of the glove up with the thumb side of the             right hand.<br />
                      </li>
                      <li> Slip the open end of the glove over the right hand and thumb.<br />
                      </li>
                      <li> Stretch the palm side of the glove with the left hand, pull             the glove on to finger level.<br />
                      </li>
                      <li> Position the fingers of the glove in line with fingers of the             right hand.<br />
                      </li>
                      <li> Pull the remainder of the glove onto the right hand.<br />
                      </li>
                      <li> Proceed with activity requiring gloves.<br />
                      </li>
                    </ol>
        <h3>Doffing             Gloves</h3>
                    <p>There are two standard             methods to taking off gloves. Method One for glove removal is recommended,             because it is harder for a person to become contaminated. However, if             Method Two can be done without causing contamination, use the easiest             method. Below are both methods. </p>
                    <h4> Method             One</h4>
                    <h4><img src="../media/image/method-1.1.png" alt="" width="150" height="150" /><img src="../media/image/method-1.2.png" alt="" width="150" height="150" /><img src="../media/image/method-1.3.png" alt="" width="150" height="150" /></h4>
                    <h4>&nbsp;</h4>
                  <ol>
                      <li> Grasp one             of the gloves and cuff and pull it partway off. The glove will turn             inside out. It is important to keep the first glove partially on your             hand before removing the second glove. This protects you from touching             the outside of either glove with your bare hands.<br />
                    </li>
                      <li> Leaving the first glove over your fingers, grasp the second             glove near the cuff and pull it part of the way off. The glove will             turn inside out. It is important to keep the second glove partially             on your hand to protect you form touching the outside surface of the             first glove with your bare hand.<br />
                    </li>
                      <li> Pull off the two gloves at the same time, being careful to touch             only the inside surfaces of the gloves with your bare hands. <br />
                    </li>
                      <li> Dispose of the gloves by placing inside out in the trash. <br />
                    </li>
                    <li> Wash hands thoroughly.<br />
                    </li>
                  </ol>
        <h4> Method             Two</h4>
        <h4><img src="../media/image/method-2.1.png" alt="" width="100" height="139" /><img src="../media/image/method-2.2.png" alt="" width="184" height="139" /></h4>
        <ol>
          <li>Grasp outside             edge near wrist.<br />
            </li>
          <li> Peel away from hand turning glove inside-out.<br />
            </li>
          <li> Hold in opposite gloved hand.<br />
            </li>
          <li> Slide ungloved finger under the wrist of the remaining glove,             be careful not to touch the outside of the glove.<br />
            </li>
          <li> Peel off from inside, creating a bag for both gloves.<br />
            </li>
          <li> Discard.<br />
            </li>
          <li> Wash hands thoroughly.<br />
          </li>
        </ol>
        <h3>Do&#8217;s             and Don&#8217;ts of Glove Use</h3>
                    <ul>
                      <li> Work from             clean to dirty&#8212;this will help prevent contamination<br />
                       </li>
                      <li> Don&#8217;t touch your face or adjust PPE with contaminated             gloves<br />
                       </li>
                      <li> Don&#8217;t touch environmental surfaces &#8211; doorknobs,             keyboards, computer mouse. This means when leaving the laboratory TAKE             OFF YOUR GLOVES. If you need to wear a glove in the hallway to carry             a sample, make sure that the gloved hand is carrying the sample and             the non gloved hand is operating door handles, elevator buttons, etc. <br />
                       </li>
                      <li>Change gloves when heavily soiled or if they are torn.<br />
                       </li>
                      <li> Discard gloves after use, never wash or reuse disposable gloves.<br />
                      </li>
                    </ul>
