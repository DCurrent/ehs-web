                    <p>The University of Kentucky is committed to providing work, study, research, and residential environments that are free from recognized hazards.  Therefore, a rigorous program of monitoring is in place to address hazards created by the use or presence of lead on-campus.
</p>
                    <p>There are two primary categories of lead use and presence on-campus.  The first is lead in building materials, such as paint, water pipes, and plumbing fixtures.   The second is the use of lead-containing products such as lead solder, ammunition, radionuclide shielding, and laboratory or studio use of organic or inorganic lead compounds. </p>
                    <h2>Lead Based Paint</h2>
                    
                    
                    
                    
                  <p>Lead-based paint is <a href="http://www.hud.gov/offices/lead/lbp/hudguidelines/Ch07.pdf">defined</a> as any paint which contains lead in  excess of one milligram per square centimeter, or &gt;0.5% by weight.  Lead-based paint can be present in campus  buildings that were built before 1978, when the Consumer Products Safety  Commission <a href="http://www.gpo.gov/fdsys/pkg/CFR-2012-title16-vol2/pdf/CFR-2012-title16-vol2-part1303.pdf">banned</a> the use of all paints in residential  and public buildings that contain greater than 0.06% lead.  </p>
                  <p>The UK Environmental Management Department  (EMD) performs extensive testing and inspection of buildings in order to &ldquo;map&rdquo;  the presence and condition of lead-based paint.   Special attention is given to facilities that are, or have the potential  to be, occupied by children <u>&lt;</u>6 years of age, who are particularly  susceptible to neurological insult from ingestion of lead.</p>
                  <p>EMD  also reviews and approves all construction, demolition, and renovation projects  that could potentially disturb any paint that contains lead. For information on the Lead-Based Paint  Management Program, click <a href="/env/lead-based_management.php">here</a>.</p>
                  <h2>Lead In Water</h2>
                  
                  <p>Potential sources of lead in campus drinking  water are the following: lead pipe, lead solder and leaded brass fittings. Lead  pipe and lead solder were banned in 1986 by amendments to the Safe Drinking  Water Act, promulgated by the U.S. Environmental Protection Agency (EPA).  The Reduction of Lead in Drinking Water Act has  reduced the acceptable levels of lead in &ldquo;lead-free&rdquo; pipes, fittings, and  fixtures from 8.0% to 0.25% as of January 4, 2014.</p>
                  <p>The <a href="http://water.epa.gov/lawsregs/rulesregs/sdwa/lcr/index.cfm">EPA Lead and Copper Rule</a> also requires public water utilities  such as Kentucky American Water Company (KAW, the water utility for Lexington-Fayette)  to include corrosion control in their water treatment protocol sufficient to  prevent lead leaching from pipes, solder or fittings.  The rule requires KAW to test a representative  sample of buildings periodically, to prove they are in compliance.  KAW has passed all Lead and Copper Rule tests.</p>
                  <p>While there could be older pipes or fixtures  within specific campus buildings that contain lead, periodic flushing of water  systems plus corrosion control treatment techniques mean that lead levels above  the EPA action level of 15 parts per billion are highly unlikely.</p>
                  <p>For  additional information on lead in drinking water, please refer to KAW's &quot;<a href="http://www.amwater.com/files/WQ_QandA_KY.pdf">Frequently Asked Questions on Water Quality</a>&quot;  or read this <a href="http://ehs.uky.edu/env/fs_water.php">Fact Sheet</a> on Drinking Water Quality from the UK <a href="http://ehs.uky.edu/env/">Environmental Management Department</a>.  KAW or the UK Environmental Management Department can perform drinking water testing  if a justifiable request is received.</p>
                  <p>UK Health &amp; Safety (UKHS) is responsible  for the safety of all other uses of lead on-campus.  UKHS performs periodic air and surface wipe  sampling during these processes, and ensures that affected persons are trained  on the safe use of these products.   Examples:</p>
                  <ul>
                    <li>Exposure to lead fumes or surface lead  contamination from discharge of ammunition during practice/training (UK Rifle  personnel, UKPD);</li>
                    <li>Use of lead solder in electronics  assembly and repair;</li>
                    <li>Use and manipulation of lead bricks  and shields in areas where radionuclides are used;</li>
                    <li>Use of elemental lead and inorganic or  organic lead compounds in laboratories or the arts.</li>
                  </ul>
                  <h2>Other Uses of lead</h2>
                  <p>For personnel who could be exposed to airborne  lead, UKHS monitors all tasks to quantify exposures and ensure they are below  the current OSHA permissible exposure limits (PELs).  If exposures above any PEL are documented, UKHS  along with the affected department and University Health Services will take all  necessary steps to reduce exposures:</p>
                  <ul>
                    <li>engineering controls</li>
                    <li>product substitution </li>
                    <li>work practice controls</li>
                    <li>personal protective equipment</li>
                    <li>medical removal protection if  necessitated by elevated blood lead levels</li>
                  </ul>
                  <p>The current OSHA PELs for lead and lead  compounds are the following:</p>
                  <ul>
                    <li>50 micrograms per cubic meter (ug/m3)  as an 8-hour time-weighted average (TWA) for elemental lead, inorganic lead  compounds, and organic lead soaps;</li>
                    <li>75 ug/m3 as an 8-hour TWA  for tetraethyl and tetramethyl lead.</li>
                  </ul>
                  <p>UKHS also performs surface wipe sampling for  lead, using <a href="http://www.cdc.gov/niosh/docs/2003-154/pdfs/9102.pdf">NIOSH Method 9102</a>.   This helps ensure that hygiene requirements are being met in  laboratories, ranges, studios, and other areas where surface lead contamination  is possible.</p>
                  <p>Contact <a href="mailto:brent.webber@uky.edu">Brent Webber</a> at UKHS if you have any questions about  laboratory or workplace exposures to lead and lead compounds.                </p>
