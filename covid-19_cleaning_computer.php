                    <h1><span data-contrast="auto" xml:lang="EN-US" lang="EN-US"><span data-ccp-parastyle="header"><span data-contrast="none" xml:lang="EN-US" lang="EN-US">COVID-19 Guidelines For Cleaning Computer Products</span></span></span></h1>                      
                    <p><span data-contrast="none" xml:lang="EN-US" lang="EN-US"> </span><span data-contrast="none" xml:lang="EN-US" lang="EN-US">Computers   and related products are often considered &ldquo;high touch&rdquo; items especially   in common shared areas such as libraries and computer labs. As such,   routine cleaning and disinfecting of computer surfaces in common shared   locations is recommended. These guidelines can also be used for the   personal care and disinfecting of your individual computer products.</span></p>
				  <h2>General Guidelines</h2>
				  <p>(Adapted from Apple <a href="https://support.apple.com/en-us/HT204172">“How to Clean your Apple Products”</a>): Computer products are made with a variety of materials, and each material might have specific cleaning requirements. To get started, here are some tips that apply to all products</p>
				  <ul>
				    <li>Use only a soft, lint-free cloth. Avoid abrasive cloths, paper towels, or similar items.</li>
				    <li>Avoid excessive wiping, which might cause damage.</li>
				    <li>Unplug all external power sources, devices, and cables.</li>
				    <li>Keep liquids away from the product, unless otherwise noted for specific products.</li>
				    <li>Don't get moisture into any openings.</li>
				    <li>Don't use aerosol sprays, bleaches, or abrasives.</li>
				    <li>Don't spray cleaners directly onto the item.</li>
			    </ul>
				  <h2>Approved Disinfectants For Coronavirus</h2>
				  <p>Do to the sensitive nature of computer electronics special consideration should be taken to clean these products to minimize the potential of damaging the computer during cleaning or with the cleaning product.</p>
				  
				  <ul>
					  <li>Using a 70% isopropyl alcohol wipe or Clorox Disinfecting Wipes, you may gently wipe the hard, nonporous surfaces of your product, such as the display, keyboard, or other exterior surfaces. Be sure to follow all manufacturer’s recommendations and warning labels when using any cleaning product.</li>
					  <li>Don't use bleach.</li>
					  <li>Avoid getting moisture in any opening, and don't submerge your product in any cleaning agents.</li>
					  <li>Don't use on fabric or leather surfaces.</li>
					  <li>70% alcohol wipes and Clorox Disinfecting Wipes are both <a href="https://www.epa.gov/pesticide-registration/list-n-disinfectants-use-against-sars-cov-2">approved by the EPA</a> for the disinfection of surfaces against Coronavirus.</li>
				  </ul>
				                     
