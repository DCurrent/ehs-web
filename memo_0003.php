          <p>Waste Disposal Funding 
        at the University of Kentucky<br />
Rationale and History of the Environmental Surcharge          </p>
<h2 align="center"> Introduction </h2>
<p> During FY1997-98, 
  the University received numerous comments on the hazardous materials surcharge 
  that is applied to certain purchases to pay for waste disposal. On 1 July 
  1998, the University of Kentucky modified the surcharge, returning to 
  the procedure originally adopted in 1994. The narrative below provides 
  the background on this issue and describes the historical evolution of 
  the surcharge, as well as the rationale for surcharge modifications. </p>
<h2 align="center"> Environmental 
  Surcharge<br />
  1 October 1994 </h2>
<p> Several years ago, 
  UK was forced to deal with the &quot;unfunded&quot; costs of hazardous and radioactive 
  waste disposal. These costs had increased exponentially in the decade 
  between 1985 and 1994. In 1994, UK reacted by implementing an environmental 
  surcharge to pay for waste disposal and to fund a new recycling program. 
  The surcharge was imposed on the purchase of all materials, supplies and 
  capital equipment at the time of their acquisition. </p>
<p align="center"> <img src="media/image/graph.gif" alt="" /></p>
<p> The surcharge was 
  applied to specific &quot;object code&quot; groups and was assessed uniformly on 
  all accounts of the University. The surcharge was not added to purchase 
  orders; it was calculated monthly from the accounting statement and charged 
  in one lump sum to each account once a month. </p>
<p> A committee of unit 
  representatives considered alternative approaches, and the method described 
  above was found to best meet the goals of fairness, ease of implementation, 
  and placing charges close to where costs are incurred. The committee felt 
  it was important to provide a cost incentive for departments and colleges 
  to reduce their waste streams. Since UK also needed to make a greater 
  effort toward recycling materials like aluminum and paper, a portion of 
  the surcharge was used to fund an expanded recycling program. </p>
<p> This method of funding 
  waste programs was intended to be fiscally sound and efficient. Funds 
  collected through the surcharge were designated for use only for these 
  programs. At the end of each year, any unused funds were to be allocated 
  for use in the program the following year. The surcharge was to be adjusted 
  annually to collect only the estimated program costs. The surcharge rate 
  was set at 0.3% in 1994. This means that a department or lab spending 
  $1,000 for materials and supplies would pay a surcharge of $3. </p>
<h2 align="center"> Environmental 
  Surcharge Modified<br />
  1 July 1997 </h2>
<p> In 1997 UK modified 
  the environmental surcharge. During a review of the federal Cost Accounting 
  Standards (CAS), it was determined that separate surcharges for hazardous 
  materials and recycling would be more appropriate. The hazardous materials 
  surcharge was set at 4.3% and was applied to the following object codes: </p>
<table border="0" cellpadding="3" cellspacing="0">
  <tr bgcolor="#DDDDFF">
    <td>3308</td>
    <td>Chemicals and 
      Laboratory Supplies</td>
  </tr>
  <tr bgcolor="#CECEFF">
    <td>3322</td>
    <td>&gt;Gases</td>
  </tr>
  <tr bgcolor="#DDDDFF">
    <td>3339</td>
    <td>Radioisotopes</td>
  </tr>
</table>
<p> The rate was set 
  at a level to recover the estimated costs of waste disposal. These three 
  object codes were selected because they best represent the materials likely 
  to lead to hazardous and radioactive waste based on our historical data. 
  For calendar year 1996 these three categories accounted for 95% of UK's 
  disposal costs. </p>
<p> The other 5% of the 
  waste costs came from a broad list of waste types, including paints, pesticides, 
  lead debris, oils, antifreeze, batteries, light ballasts, roofing material 
  and diesel fuel. Collecting a proportionate share of the surcharge for 
  each of these products at the time of purchase is problematic. UK does 
  not have separate object codes for these products. Applying the surcharge 
  to object code groups that contain these products would result in revenues 
  far exceeding the proportionate share of waste costs. </p>
<p> The recycling surcharge 
  was set at 0.2% and was assessed on all other purchases of materials, 
  supplies and capital equipment. It was not assessed on object codes that 
  were assessed the hazardous materials surcharge. </p>
<h2 align="center"> Return 
  to a Broad-Based Surcharge<br />
  1 July 1998 </h2>
<p> Hazardous materials 
  surcharge receipts for July, August and September 1997 were significantly 
  below anticipated levels. In order to pay for the waste program, it was 
  necessary to increase the charge from 4.3% to 8% on 1 October 1997. This 
  increase adversely affected several departmental teaching programs and 
  disrupted budgets for a number of investigators who had significant chemical 
  supply needs. There were also indications that the surcharge would have 
  to be increased again in 1998. Also, there was a demonstrated need to 
  split the object code that brought in the most receipts, namely Chemicals 
  and Lab Supplies. If lab supplies (which as a rule do not generate hazardous 
  waste) were not assessed, then the surcharge rate applied to chemicals, 
  gases and radioisotopes would have to be much higher, perhaps approaching 
  15-20%. The higher surcharge rate could have been justified as a cost-based 
  expense of teaching and research, as well as a deterrent to the purchase 
  of unnecessary chemicals and the generation of hazardous waste. After 
  considerable discussion, however, it was decided that a broad-based surcharge, 
  which spreads the costs of waste disposal across a larger base of University 
  expenditures, would impose less hardship on teaching and research programs. 
  Thus, on 1 July 1998 UK returned to the combined environmental surcharge 
  on all materials, supplies and capital equipment. The rate was set at 
  0.67%. </p>
<p> In order for the 
  environmental surcharge to be applied to purchases made on federal grant 
  accounts, it must satisfy the requirements of the Cost Accounting Standards 
  (CAS). The surcharge must pass three major tests under CAS. </p>
<ul>
  <li>May charge for 
    waste as a &quot;direct cost&quot; or &quot;indirect cost&quot; but not both. UK intends to charge grants for hazardous and radioactive waste disposal 
    as a direct cost (i.e., the surcharge) and not an indirect cost. UK 
    did not include waste disposal in the calculation of the indirect cost 
    rate (sometimes referred to as the &quot;overhead rate&quot;).
    <p>Charges that are 
      imposed on grants as direct costs must meet the requirements for service 
      centers and recharge operations.</p>
  </li>
  <li>
    <p>May not charge 
      more than actual cost for waste disposal. UK is paying a portion 
      of the cost of waste disposal with state funds, so there is no &quot;profit&quot; 
      in this operation. Accounts are charged less than the actual cost 
      of waste disposal.</p>
  </li>
  <li>
    <p>Charges for 
      waste disposal must be applied uniformly. UK is applying the surcharge 
      uniformly to all purchases-everyone pays the same rate. There is no 
      discriminatory or inconsistent pricing. </p>
  </li>
</ul>
<h2 align="center"> In 
  Future </h2>
<p> In order to establish 
  an orderly process for future modification of the environmental surcharge the following steps will be taken: </p>
<ul>
  <li>Consistent with 
    CAS, any balances remaining at the end of a fiscal year will be redistributed 
    to the waste disposal and recycling programs for the new fiscal year.</li>
  <li>The surcharge rate 
    will be established during the preparation of the annual budget and 
    communicated to the sectors.</li>
  <li>Representatives 
    from each sector will meet quarterly, or as needed, to review the status 
    of surcharge revenues and waste disposal plus recycling expenses. </li>
</ul>
